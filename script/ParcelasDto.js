class ParcelasDto {
    constructor() {
      this.numero = null;
      this.valorAmortizacao = null;
      this.valorJuros = null;
      this.valorPrestacao = null;
    }
  
    setNumero(numero) {
      this.numero = numero;
    }
  
    getNumero() {
      return this.numero;
    }
  
    setValorAmortizacao(valorAmortizacao) {
      this.valorAmortizacao = valorAmortizacao;
    }
  
    getValorAmortizacao() {
      return this.valorAmortizacao;
    }
  
    setValorJuros(valorJuros) {
      this.valorJuros = valorJuros;
    }
  
    getValorJuros() {
      return this.valorJuros;
    }
  
    setValorPrestacao(valorPrestacao) {
      this.valorPrestacao = valorPrestacao;
    }
  
    getValorPrestacao() {
      return this.valorPrestacao;
    }
  }
  